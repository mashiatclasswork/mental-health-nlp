CLEAN_CFG = dict(
    lower=True,
    strip_html=True,
    strip_md=True,
    strip_urls=True,
    squash_spaces=True,
    min_chars=5,
)

html_tag_re = re.compile(r"<.*?>")
md_link_re  = re.compile(r"\[([^\]]+)\]\(([^)]+)\)")
url_re      = re.compile(r"http\S+|www\.\S+")
word_re     = re.compile(r"\b\w+\b")


def clean_for_bert(s: str) -> str:
    s = str(s)
    if CLEAN_CFG["lower"]:
        s = s.lower()
    if CLEAN_CFG["strip_html"]:
        s = html_tag_re.sub(" ", s)
    if CLEAN_CFG["strip_md"]:
        s = md_link_re.sub(r"\1", s)
    if CLEAN_CFG["strip_urls"]:
        s = url_re.sub(" ", s)
    if CLEAN_CFG["squash_spaces"]:
        s = " ".join(s.split())
    return s


def word_count(s: str) -> int:
    return len(word_re.findall(str(s)))



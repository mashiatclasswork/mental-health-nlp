def train_one_model(
    model,
    train_loader,
    val_loader,
    num_epochs=NUM_EPOCHS,
    lr=LR,
):
    # Weighted + smoothed loss
    loss_fn = torch.nn.CrossEntropyLoss(
        weight=CLASS_WEIGHTS,
        label_smoothing=LABEL_SMOOTH,
    )

    optimizer = AdamW(
        filter(lambda p: p.requires_grad, model.parameters()),
        lr=lr,
        weight_decay=WEIGHT_DECAY,
    )

    total_steps = (len(train_loader) // GRAD_ACCUM_STEPS) * num_epochs
    warmup_steps = int(WARMUP_RATIO * total_steps)

    scheduler = get_linear_schedule_with_warmup(
        optimizer,
        num_warmup_steps=warmup_steps,
        num_training_steps=total_steps,
    )

    history = {"train_loss": [], "val_loss": []}

    best_val_loss = float("inf")
    patience_counter = 0

    print(
        f"\n🔥 Training: epochs={num_epochs}, lr={lr}, "
        f"batch_size={BATCH_SIZE}, grad_accum={GRAD_ACCUM_STEPS}, "
        f"warmup_ratio={WARMUP_RATIO}, wd={WEIGHT_DECAY}"
    )

    model.to(device)

    for epoch in range(num_epochs):
        # ---- TRAIN ----
        model.train()
        total_train_loss = 0.0
        optimizer.zero_grad()

        for step, batch in enumerate(train_loader):
            batch = {k: v.to(device) for k, v in batch.items()}
            labels = batch["labels"]

            outputs = model(**batch)
            logits = outputs.logits

            loss = loss_fn(logits, labels)
            loss = loss / GRAD_ACCUM_STEPS
            loss.backward()

            if (step + 1) % GRAD_ACCUM_STEPS == 0:
                torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
                optimizer.step()
                scheduler.step()
                optimizer.zero_grad()

            total_train_loss += loss.item() * GRAD_ACCUM_STEPS

        avg_train_loss = total_train_loss / len(train_loader)

        # ---- VALIDATION ----
        model.eval()
        total_val_loss = 0.0
        with torch.no_grad():
            for batch in val_loader:
                batch = {k: v.to(device) for k, v in batch.items()}
                labels = batch["labels"]
                outputs = model(**batch)
                logits = outputs.logits
                val_loss = loss_fn(logits, labels)
                total_val_loss += val_loss.item()

        avg_val_loss = total_val_loss / len(val_loader)

        history["train_loss"].append(avg_train_loss)
        history["val_loss"].append(avg_val_loss)

        print(
            f"Epoch {epoch+1}/{num_epochs} | "
            f"Train Loss: {avg_train_loss:.4f} | "
            f"Val Loss: {avg_val_loss:.4f}"
        )

        # ---- EARLY STOPPING ----
        if avg_val_loss < best_val_loss:
            best_val_loss = avg_val_loss
            patience_counter = 0
            torch.save(model.state_dict(), "best_model_v1.pt")
        else:
            patience_counter += 1
            if patience_counter >= EARLY_STOPPING_PATIENCE:
                print(f"⛔ Early stopping triggered at epoch {epoch+1}")
                break

    # Load best checkpoint
    model.load_state_dict(torch.load("best_model_v1.pt", map_location=device))
    print("✔ Loaded best checkpoint for Version 1")
    print("🔥 Training finished!")
    return model, history


def evaluate_model(model, data_loader, label_names=None):
    model.eval()
    all_labels = []
    all_preds = []

    with torch.no_grad():
        for batch in data_loader:
            labels = batch["labels"].numpy()
            batch = {k: v.to(device) for k, v in batch.items()}
            outputs = model(**batch)
            logits = outputs.logits.detach().cpu().numpy()
            preds = np.argmax(logits, axis=1)

            all_labels.extend(labels)
            all_preds.extend(preds)

    all_labels = np.array(all_labels)
    all_preds  = np.array(all_preds)

    acc = accuracy_score(all_labels, all_preds)
    precision, recall, f1, _ = precision_recall_fscore_support(
        all_labels,
        all_preds,
        average="macro",
        zero_division=0,
    )

    print(f"Accuracy:       {acc:.4f}")
    print(f"Macro Precision:{precision:.4f}")
    print(f"Macro Recall:   {recall:.4f}")
    print(f"Macro F1:       {f1:.4f}")

    print("\nClassification report:")
    print(classification_report(all_labels, all_preds, target_names=label_names))

    cm = confusion_matrix(all_labels, all_preds)
    return {
        "accuracy": acc,
        "macro_precision": precision,
        "macro_recall": recall,
        "macro_f1": f1,
        "confusion_matrix": cm,
        "true_labels": all_labels,
        "pred_labels": all_preds,
    }



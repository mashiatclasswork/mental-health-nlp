def normalize_label(x):
    s = str(x).strip().lower()

    # numeric mapping 0–4 (from Kaggle)
    if s.isdigit():
        code = int(s)
        if code == 0:
            # Stress → map to None (0)
            return CANON_LABELS["None"]
        if code == 1:
            return CANON_LABELS["Depression"]
        if code == 2:
            return CANON_LABELS["Bipolar disorder"]
        if code == 3:
            return CANON_LABELS["Personality disorder"]
        if code == 4:
            return CANON_LABELS["Anxiety"]

    # textual fallback (if ever present)
    if "stress" in s:
        return CANON_LABELS["None"]
    if "depression" in s or "depressed" in s:
        return CANON_LABELS["Depression"]
    if "bipolar" in s or "manic" in s:
        return CANON_LABELS["Bipolar disorder"]
    if "personality" in s or s in {"bpd", "npd", "pd"}:
        return CANON_LABELS["Personality disorder"]
    if "anxiety" in s or "anxious" in s:
        return CANON_LABELS["Anxiety"]

    return None


